static void LCD_DrawString16(const char* s, uint16_t bg, uint16_t fg,
                             uint16_t x, uint16_t y)
{
    uint16_t cx = x;
    while (*s) {
        LCD_WriteChar((uint8_t)*s, bg, fg, cx, y);
        cx += CHAR_WIDTH_16;
        s++;
    }
}


